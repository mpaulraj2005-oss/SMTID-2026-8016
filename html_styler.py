class HTMLPageBuilder:

    def generate_meta(self,title,desc):

        return f"""
        <title>{title}</title>
        <meta name="description" content="{desc}">
        <meta name="viewport" content="width=device-width, initial-scale=1">
        """

    def build_page(self,title,desc,content):

        meta=self.generate_meta(title,desc)

        html=f"""
        <!DOCTYPE html>
        <html>
        <head>
        {meta}
        </head>

        <body>

        {content}

        </body>
        </html>
        """

        return html

