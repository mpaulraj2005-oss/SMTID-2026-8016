def generate_site(title,subtitle):

    hero=hero_section(title,subtitle)

    features=features_section([
        "Fast Generation",
        "Responsive Design",
        "AI Powered"
    ])

    contact=contact_section()

    content=hero+features+contact

    palette=palette_gen.get_palette("modern")

    css=css_gen.generate_css(palette)

    html=builder.build_page(title,"AI Generated Website",content)

    final="<style>"+css+"</style>"+html

    return final


