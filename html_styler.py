def hero_section(title,subtitle):

    return f"""
    <section class="hero">
    <h1>{title}</h1>
    <p>{subtitle}</p>
    </section>
    """

def features_section(features):

    items=""

    for f in features:
        items+=f"<div class='feature'>{f}</div>"

    return f"""
    <section class="features">
    {items}
    </section>
    """

def contact_section():

    return """
    <section>
    <h2>Contact Us</h2>

    <input placeholder="Name">
    <input placeholder="Email">
    <textarea placeholder="Message"></textarea>

    <button>Send</button>

    </section>
    """

